    private void jBAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAddActionPerformed
        JDNuevaTarea jdnt = new JDNuevaTarea(null, true);
        jdnt.setVisible(true);
        //Si ha pulsado aceptar se añadirá la tarea creada al estado actual
        if (jdnt.haPulsadoAceptar()) {
            tarea.addSubtarea(jdnt.getTarea());
        }
        rellenarTabla();
    }//GEN-LAST:event_jBAddActionPerformed

    private void jBEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBEliminarActionPerformed
         if (jTabla.getSelectedRow() >= 0) {
            JDConfirmaEliminar jdce = new JDConfirmaEliminar(null, true);
            jdce.setVisible(true);
            if (jdce.haPulsadoAceptar()) {
                eliminarTarea((String) jTabla.getValueAt(jTabla.getSelectedRow(), 0));
                rellenarTabla();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Por favor seleccione una tarea para poder eliminarla", null, JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jBEliminarActionPerformed

