     /**
     * Se encarga de eliminar la tarea seleccionada de la lista actual
     *
     * @param indice número de columna seleccionada del JTable
     */
    private void eliminarTarea(String titulo) {
       int indice=jTabla.getSelectedRow();
       if(indice>=0){
           tarea.getSubtareas().remove(indice);
       }else{
           JOptionPane.showMessageDialog(this, "Para eliminar una subtarea debe primero seleccionar una", titulo, JOptionPane.INFORMATION_MESSAGE);
       }      
      jTabla.updateUI();
    }
    /**
     * Se encarga de rellenar los campos con los datos actuales de la tarea
     */
    private void rellenarDatos(){
        jTFTarea.setText(tarea.getTitulo());
        jDCFecha.setCalendar(tarea.getCalendar());
        jTFDescripcion.setText(tarea.getDescripcion());
        rellenarTabla();
    }
    
     /**
     * Se encarga de rellenar el modelo de la Tabla indicada
     */
    private void rellenarTabla() {
        TableColumnModel tcm;
        if(tarea.getSubtareas()!=null && !tarea.getSubtareas().isEmpty()){
            tableModel = new JDEditar.TableModel(tarea.getSubtareas());
            jTabla.setModel(tableModel);
        }
        tcm = jTabla.getColumnModel();
        for (int col = 0; col < headers.length; col++) {
            tcm.getColumn(col).setHeaderValue(headers[col]);
        }
        jTabla.updateUI();
    }
    
    public boolean haPulsadoAceptar(){
        return this.haPulsadoAceptar;
    }
    
